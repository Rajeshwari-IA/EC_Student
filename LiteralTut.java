package pack;

public class LiteralTut {

	public static void main(String[] args) {
		int count=827;
		int octal=067;
		int hexa=0x67e;
		System.out.println(count);
		System.out.println(octal);
		char c1='z';
		System.out.println(c1);
		System.out.println(hexa);
		int binary=0b11;
		System.out.println(binary);
		

	}

}
